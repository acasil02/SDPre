public class Objective6Lab1 {
  public static void main(String[] args) {
    int month = 6;

    switch (month) {
      case 1:
        System.out.println("January");
        break;

      case 6:
          System.out.println("January");
          break;

      default:
        System.out.println("That is not a valid month");
        break;
    }
  }
}
